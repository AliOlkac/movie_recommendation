import './globals.css'; // Projenin global stil dosyasını içe aktarıyor
import type { Metadata } from 'next'; // Next.js'in Metadata türünü içe aktarıyor
import { Inter } from 'next/font/google'; // Google'dan "Inter" fontunu içe aktarıyor

// "Inter" yazı tipi ayarlarını yapılandırıyor, burada Latin alfabesi destekleniyor
